bool Table::queen_move(pos pos_i, pos pos_f) {

    if (!valid_move(pos_i, pos_f, queen_moves)) return false;

    if (!free_way(pos_i, pos_f)) {
        cout << "Invalid\n";
        return false;
    }
    move_piece(pos_i, pos_f);
    return true;
}